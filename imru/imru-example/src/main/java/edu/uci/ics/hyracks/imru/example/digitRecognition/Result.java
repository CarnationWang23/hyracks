package edu.uci.ics.hyracks.imru.example.digitRecognition;

import java.io.Serializable;

/**
 * Object which is generated by map(), aggregated in reduce() and used in
 * update()
 * 
 * @author wangrui
 */
public class Result implements Serializable {
    int correct;
    int total;
    double loss;
    double[][] wns;

    int numPatternsSampled;
    double[][] diagHessians;

    public Result(NNLayer[] layers) {
        correct = 0;
        total = 0;
        wns = new double[layers.length][];
        diagHessians = new double[layers.length][];
        for (int i = 0; i < layers.length; i++) {
            wns[i] = new double[layers[i].weights.length];
            diagHessians[i] = new double[layers[i].weights.length];
        }
    }

    public void add(NNResult result) {
        for (int i = 0; i < wns.length; i++) {
            for (int j = 0; j < wns[i].length; j++) {
                wns[i][j] += result.wns[i][j];
            }
        }
    }

    public void add(Result result) {
        correct += result.correct;
        total += result.total;
        loss += result.loss;
        numPatternsSampled += result.numPatternsSampled;
        for (int i = 0; i < wns.length; i++) {
            for (int j = 0; j < wns[i].length; j++) {
                wns[i][j] += result.wns[i][j];
            }
        }
    }

    public double sum() {
        double sum = 0;
        for (int i = 0; i < wns.length; i++) {
            for (int j = 0; j < wns[i].length; j++) {
                sum += wns[i][j];
            }
        }
        return sum;
    }
}
